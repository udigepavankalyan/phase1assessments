package typecasting;

public class Typecasting {
	public static void main(String[] args) {
		int n1=53;
		double n2=72.43224453;
		boolean n3=true;
		char n4='s';
		long n5=55456436436L;
		float n6=4434.54478F;
		byte n11=123;
		int n7=(int)n2;
		System.out.println("value is :"+n7);
		char n8=(char)n1;
		System.out.println("value is :"+n8);
		long n9=(long)n6;
		System.out.println("value is :"+n9);
		float n10=(float)n2;
		System.out.println("value is :"+n10);
		int n12=(int)n11;
		double n13=(double)n11;
	}
}

